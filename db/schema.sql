    created_at TEXT NOT NULL,
    execution_id TEXT NOT NULL,
    source TEXT NOT NULL,
    FOREIGN KEY(company_uuid) REFERENCES companies(company_uuid)
);

-- DII (Domestic Institutional Investor) holdings raw records
CREATE TABLE IF NOT EXISTS dii_holdings (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    company_uuid TEXT NOT NULL,
    quarter TEXT NOT NULL,
    dii_holding_pct REAL NOT NULL,
    is_current INTEGER NOT NULL CHECK(is_current IN (0, 1)) DEFAULT 1,
    created_at TEXT NOT NULL,
    execution_id TEXT NOT NULL,
    source TEXT NOT NULL,
    FOREIGN KEY(company_uuid) REFERENCES companies(company_uuid)
);

-- Mutual Fund holdings raw records (typically a subset of DII)
CREATE TABLE IF NOT EXISTS mutual_fund_holdings (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    company_uuid TEXT NOT NULL,
    quarter TEXT NOT NULL,
    mf_holding_pct REAL NOT NULL,
    is_current INTEGER NOT NULL CHECK(is_current IN (0, 1)) DEFAULT 1,
    created_at TEXT NOT NULL,
    execution_id TEXT NOT NULL,
    source TEXT NOT NULL,
    FOREIGN KEY(company_uuid) REFERENCES companies(company_uuid)
);

-- Public / Retail holdings raw records
CREATE TABLE IF NOT EXISTS public_holdings (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    company_uuid TEXT NOT NULL,
    quarter TEXT NOT NULL,
    public_holding_pct REAL NOT NULL,
    is_current INTEGER NOT NULL CHECK(is_current IN (0, 1)) DEFAULT 1,
    created_at TEXT NOT NULL,
    execution_id TEXT NOT NULL,
    source TEXT NOT NULL,
    FOREIGN KEY(company_uuid) REFERENCES companies(company_uuid)
);

-- Promoter pledge raw records (pledged shares as a % of promoter's holding)
CREATE TABLE IF NOT EXISTS promoter_pledges (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    company_uuid TEXT NOT NULL,
    quarter TEXT NOT NULL,
    pledged_pct REAL NOT NULL,
    is_current INTEGER NOT NULL CHECK(is_current IN (0, 1)) DEFAULT 1,
    created_at TEXT NOT NULL,
    execution_id TEXT NOT NULL,
    source TEXT NOT NULL,
    FOREIGN KEY(company_uuid) REFERENCES companies(company_uuid)
);

-- Unified quarterly ownership snapshots (single source of truth)
CREATE TABLE IF NOT EXISTS ownership_snapshots (
    snapshot_uuid TEXT PRIMARY KEY,
    company_uuid TEXT NOT NULL,
    quarter TEXT NOT NULL,
    version TEXT NOT NULL,
    is_current INTEGER NOT NULL CHECK(is_current IN (0, 1)) DEFAULT 1,
    promoter_holding_pct REAL NOT NULL,
    fii_holding_pct REAL NOT NULL,
    dii_holding_pct REAL NOT NULL,
    mf_holding_pct REAL NOT NULL,
    public_holding_pct REAL NOT NULL,
    pledged_pct REAL NOT NULL,
    created_at TEXT NOT NULL,
    execution_id TEXT NOT NULL,
    source TEXT NOT NULL,
    FOREIGN KEY(company_uuid) REFERENCES companies(company_uuid)
);

-- Quarterly company-level analytics (deltas)
CREATE TABLE IF NOT EXISTS company_quarterly_analytics (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    company_uuid TEXT NOT NULL,
    quarter TEXT NOT NULL,
    promoter_delta REAL,
    fii_delta REAL,
    dii_delta REAL,
    mf_delta REAL,
    pledged_delta REAL,
    is_current INTEGER NOT NULL CHECK(is_current IN (0, 1)) DEFAULT 1,
    created_at TEXT NOT NULL,
    execution_id TEXT NOT NULL,
    source TEXT NOT NULL,
    FOREIGN KEY(company_uuid) REFERENCES companies(company_uuid)
);

-- Consolidated ranking reports (JSON reports of Top buying/reduction companies)
CREATE TABLE IF NOT EXISTS analytics_reports (
    report_uuid TEXT PRIMARY KEY,
    quarter TEXT NOT NULL,
    report_type TEXT NOT NULL,
    report_data TEXT NOT NULL, -- JSON string
    is_current INTEGER NOT NULL CHECK(is_current IN (0, 1)) DEFAULT 1,
    created_at TEXT NOT NULL,
    execution_id TEXT NOT NULL
);

-- Active trades table to track opened positions, targets, and exit alerts
CREATE TABLE IF NOT EXISTS active_trades (
    trade_id TEXT PRIMARY KEY,
    symbol TEXT NOT NULL,
    entry_quarter TEXT NOT NULL,
    entry_price REAL NOT NULL,
    current_price REAL NOT NULL,
    target_price_10 REAL NOT NULL,
    target_price_20 REAL NOT NULL,
    status TEXT NOT NULL CHECK(status IN ('Open', 'Closed')) DEFAULT 'Open',
    entry_date TEXT NOT NULL,
    exit_date TEXT,
    exit_price REAL,
    exit_reason TEXT CHECK(exit_reason IN ('Target 10 Hit', 'Target 20 Hit', 'Promoter Sell', 'Time Stop')),
    created_at TEXT NOT NULL,
    execution_id TEXT NOT NULL,
    FOREIGN KEY(symbol) REFERENCES companies(symbol)
);
